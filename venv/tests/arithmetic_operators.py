'''
This is to demonstrate the usage of arithmetic operators
'''

print(4+10)             #Addition
print(19-29)            #Subtraction
print(34*19)            #Multiplication
print(1452/23)          #Divsion
print(1452//23)         #Division truncated
print(145%10)           #Modulo
print(15**4)            #Exponents

x = 10
y = 20
x = x + 3               #Regular assignment after addition
y += 10                 #Augmented assignment operator

print(x,y)

x = 10 + 3 * 2          #BODMAS rules still apply, operator precedence
print(x)

